-- This SQLite script creates a multi-table database for Banking Standard Operating Procedures (SOP).
-- It includes tables for policy registration, compliance requirements, and escalation matrices.
-- All tables are populated with 150 consistent entries from the master SOP file.

-- Drop existing tables to ensure a clean slate on re-execution.
DROP TABLE IF EXISTS PolicyRegistry;
DROP TABLE IF EXISTS SOPCompliance;
DROP TABLE IF EXISTS EscalationMatrix;

-- --------------------------------------------------------------------------------
-- Table 1: PolicyRegistry
-- Contains the primary identification and categorization of banking policies.
-- --------------------------------------------------------------------------------
CREATE TABLE [PolicyRegistry]
(
    [PolicyID] TEXT NOT NULL PRIMARY KEY,
    [Category] TEXT NOT NULL,
    [ServiceType] TEXT NOT NULL
);

-- --------------------------------------------------------------------------------
-- Table 2: SOPCompliance
-- Tracks the standard requirements and thresholds for potential breaches.
-- --------------------------------------------------------------------------------
CREATE TABLE [SOPCompliance]
(
    [RecordID] INTEGER PRIMARY KEY AUTOINCREMENT,
    [PolicyID] TEXT NOT NULL,
    [StandardRequirement] TEXT,
    [BreachThreshold] TEXT,
    FOREIGN KEY (PolicyID) REFERENCES PolicyRegistry(PolicyID)
);

-- --------------------------------------------------------------------------------
-- Table 3: EscalationMatrix
-- Defines the personnel or departments responsible for handling breaches.
-- --------------------------------------------------------------------------------
CREATE TABLE [EscalationMatrix]
(
    [RecordID] INTEGER PRIMARY KEY AUTOINCREMENT,
    [PolicyID] TEXT NOT NULL,
    [EscalationLevel] TEXT,
    FOREIGN KEY (PolicyID) REFERENCES PolicyRegistry(PolicyID)
);

