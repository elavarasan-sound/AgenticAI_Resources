-- This SQLite script creates a multi-table database for Bank Customer Management.
-- It includes tables for customer profiles, transaction history, and feedback escalations.
-- All tables are populated with 100 consistent entries to simulate a robust production environment.

-- Drop existing tables to ensure a clean slate on re-execution.
DROP TABLE IF EXISTS CustomerAccounts;
DROP TABLE IF EXISTS AccountHistory;
DROP TABLE IF EXISTS FeedbackEscalation;

-- --------------------------------------------------------------------------------
-- Table 1: CustomerAccounts
-- Primary details of bank customers
-- --------------------------------------------------------------------------------
CREATE TABLE [CustomerAccounts]
(
    [CustomerID] TEXT NOT NULL PRIMARY KEY,
    [CustomerName] TEXT NOT NULL,
    [Branch] TEXT NOT NULL,
    [AccountNumber] TEXT NOT NULL,
    [AccountType] TEXT NOT NULL
);

-- --------------------------------------------------------------------------------
-- Table 2: AccountHistory
-- Tracks transactions and data retrieval timestamps
-- --------------------------------------------------------------------------------
CREATE TABLE [AccountHistory]
(
    [RecordID] INTEGER PRIMARY KEY AUTOINCREMENT,
    [CustomerID] TEXT NOT NULL,
    [TransactionDate] TEXT NOT NULL,
    [TransactionType] TEXT NOT NULL,
    [Amount] REAL,
    [Status] TEXT,
    FOREIGN KEY (CustomerID) REFERENCES CustomerAccounts(CustomerID)
);

-- --------------------------------------------------------------------------------
-- Table 3: FeedbackEscalation
-- Handles escalation for complaints or delayed responses
-- --------------------------------------------------------------------------------
CREATE TABLE [FeedbackEscalation]
(
    [RecordID] INTEGER PRIMARY KEY AUTOINCREMENT,
    [CustomerID] TEXT NOT NULL,
    [IssueType] TEXT NOT NULL,
    [EscalationLevel] TEXT,
    FOREIGN KEY (CustomerID) REFERENCES CustomerAccounts(CustomerID)
);

